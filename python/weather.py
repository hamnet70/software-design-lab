
weather = input("Is the weather rainy, cloudy, or sunny? ")


if weather == 'rainy':
    print("Get your umbrella!")
elif weather == 'cloudy':
    print("You do not need sunscreen")
elif weather == 'sunny':
    print("Wear your shades.") 
else:
    print("I don't know what you should do. I'm just a little program")

